'''Escribe un programa que calcule el área y el perímetro de un cuadrado y muestre los resultados (indicando
cuál es cuál) por pantalla.'''

#consultamos al usuario el tamaño
lado = int(input("ingrese el tamaño de uno de los lados:"))

#calculamos el perimetro del cuadrado
perimetro = lado+lado+lado+lado

#calculamos su area
area= lado*lado

#lo mostramos por pantalla
print("el perimetro del cuadrado es:",perimetro)
print("el area del cuadrado es:",area)

           

